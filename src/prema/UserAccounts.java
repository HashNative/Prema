    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
            if(jTable1.getSelectedRowCount()>0){
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to you want to delete?", "Aleart", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    try {

                        String sql1 = "delete from Login where "
                        + "Username='" + (jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0).toString()) + "' ";

                        PreparedStatement pst = con.prepareStatement(sql1);
                        pst.executeUpdate();

                        pst.close();

                        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                        dtm.setRowCount(0);
                        ViewLogin();

                        password.setText(null);
                        username.setText("");

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, e);

                    }

                }

            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

